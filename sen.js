var currentSongIndex = getRandomIndex();

function getRandomIndex() {
  return Math.floor(Math.random() * Object.keys(lagu).length);
}

function playPrevious() {
  currentSongIndex =
    (currentSongIndex - 1 + Object.keys(lagu).length) %
    Object.keys(lagu).length;
  playSelectedSong();
}

function playNext() {
  currentSongIndex = (currentSongIndex + 1) % Object.keys(lagu).length;
  playSelectedSong();
}

function playSelectedSong() {
  var songKey = Object.keys(lagu)[currentSongIndex];
  tampilkanLirik(songKey);
  tampilkanGambar(songKey);
  tampilkanJudul(songKey);
  tampilkanArtis(songKey);
  playLagu(songKey);
}

document.getElementById("prevbut").addEventListener("click", function () {
  playPrevious();
});

document.getElementById("nextbut").addEventListener("click", function () {
  playNext();
});

